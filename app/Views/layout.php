    $(function() {
        // Step show event
        $("#smartwizard").on("showStep", function(e, anchorObject, stepIndex, stepDirection, stepPosition) {
            $("#prev-btn").removeClass('disabled').prop('disabled', false);
            $("#next-btn").removeClass('disabled').prop('disabled', false);
            if (stepPosition === 'first') {
                $("#prev-btn").addClass('disabled').prop('disabled', true);
            } else if (stepPosition === 'last') {
                $("#next-btn").addClass('disabled').prop('disabled', true);
            } else {
                $("#prev-btn").removeClass('disabled').prop('disabled', false);
                $("#next-btn").removeClass('disabled').prop('disabled', false);
            }

            // Get step info from Smart Wizard
            let stepInfo = $('#smartwizard').smartWizard("getStepInfo");
            $("#sw-current-step").text(stepInfo.currentStep);
            $("#sw-total-step").text(stepInfo.totalSteps);
        });

        $("#smartwizard").on("initialized", function(e) {
            console.log("initialized");
        });

        $("#smartwizard").on("loaded", function(e) {
            console.log("loaded");
        });

        // Smart Wizard
        $('#smartwizard').smartWizard({
            selected: 0,
            autoAdjustHeight: true,
            theme: 'basic', // basic, arrows, square, round, dots

            toolbar: {
                showNextButton: true, // show/hide a Next button
                showPreviousButton: true, // show/hide a Previous button
                position: 'both', // none/ top/ both bottom
                extraHtml: `<button class="btn btn-success" onclick="onFinish()">Finish</button>
                              <button class="btn btn-secondary" onclick="onCancel()">Cancel</button>`
            },
            anchor: {
                enableNavigation: false, // Enable/Disable anchor navigation 
                enableNavigationAlways: false, // Activates all anchors clickable always
                enableDoneState: true, // Add done state on visited steps
                markPreviousStepsAsDone: false, // When a step selected by url hash, all previous steps are marked done
                unDoneOnBackNavigation: false, // While navigate back, done state will be cleared
                enableDoneStateNavigation: true // Enable/Disable the done state navigation
            },
        });
    });
